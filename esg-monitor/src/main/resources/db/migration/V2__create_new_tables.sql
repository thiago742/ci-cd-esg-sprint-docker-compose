CREATE TABLE EMPRESA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    CNPJ VARCHAR2(20) UNIQUE NOT NULL,
    ENDERECO VARCHAR2(200)
);

CREATE TABLE EQUIPAMENTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    TIPO VARCHAR2(50),
    ID_EMPRESA NUMBER,
    CONSTRAINT FK_EQUIPAMENTO_EMPRESA FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA(ID)
);

CREATE TABLE MANUTENCAO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA DATE NOT NULL,
    DESCRICAO VARCHAR2(200),
    ID_EQUIPAMENTO NUMBER,
    CONSTRAINT FK_MANUTENCAO_EQUIPAMENTO FOREIGN KEY (ID_EQUIPAMENTO) REFERENCES EQUIPAMENTO(ID)
);

CREATE TABLE RELATORIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITULO VARCHAR2(100) NOT NULL,
    CONTEUDO CLOB,
    DATA DATE,
    ID_EMPRESA NUMBER,
    CONSTRAINT FK_RELATORIO_EMPRESA FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA(ID)
);

CREATE TABLE USUARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(255) NOT NULL,
    ROLE VARCHAR2(50) NOT NULL
);
